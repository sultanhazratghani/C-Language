#include<iostream>
using namespace std;
int main()
{
	cout<<"-------type casting-------"<<endl;
	char a =97;
	cout<< a <<endl;
	
	int ch='a';
	cout<< ch <<endl;
	
	//int 32 bit allocated
	//maximum value of int =2 power 32-1
	// min value of int = 0
	
	//char 8 bit allocated
	//max value of char=2 power 8-1
	//min value of char=0
	
	char ch1=12345;
	cout<<ch1<<endl;
	
}
